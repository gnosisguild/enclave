//! Regenerates `circuits/lib/src/configs/committee/<name>/parity_{insecure,secure}.nr`
//! for the given committee. Invoked by `scripts/build-circuits.ts` whenever the active
//! committee is set, so the Reed-Solomon parity matrices stay in lockstep with `(N, T)`
//! and the threshold `QIS` of each preset.
//!
//! Usage:
//!     cargo run --release --bin generate_parity_matrices -- \
//!         --committee <minimum|micro|small> \
//!         [--output-root <path-to-circuits/lib/src/configs/committee>]
//!
//! The Noir literal is produced by
//! `e3_zk_helpers::circuits::dkg::share_computation::utils::parity_matrix_constant_string`,
//! which is the same function the C2 codegen uses — so the values here match what the
//! prover would compute at witness time.

use std::path::{Path, PathBuf};
use std::str::FromStr;

use anyhow::{Context, Result};
use clap::Parser;
use e3_fhe_params::{build_pair_for_preset, BfvPreset};
use e3_zk_helpers::ciphernodes_committee::CiphernodesCommitteeSize;
use e3_zk_helpers::circuits::dkg::share_computation::utils::parity_matrix_constant_string;

#[derive(Parser, Debug)]
#[command(
    name = "generate_parity_matrices",
    about = "Regenerate parity_{insecure,secure}.nr for a committee."
)]
struct Args {
    /// Committee name (`minimum`, `micro`, `small`). Determines `(N, T)`.
    #[arg(long)]
    committee: String,

    /// Root directory containing the per-committee modules. Defaults to
    /// `<repo>/circuits/lib/src/configs/committee` relative to the workspace.
    #[arg(long)]
    output_root: Option<PathBuf>,
}

fn workspace_root() -> Result<PathBuf> {
    // CARGO_MANIFEST_DIR resolves to crates/zk-helpers at build time.
    let manifest = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    manifest
        .parent()
        .and_then(|p| p.parent())
        .map(Path::to_path_buf)
        .context("could not locate workspace root from CARGO_MANIFEST_DIR")
}

fn output_root(args: &Args) -> Result<PathBuf> {
    if let Some(p) = &args.output_root {
        return Ok(p.clone());
    }
    Ok(workspace_root()?
        .join("circuits")
        .join("lib")
        .join("src")
        .join("configs")
        .join("committee"))
}

/// Maps a `(preset, committee)` pair to the path of the file that holds its
/// `PARITY_MATRIX` literal. The `_insecure` / `_secure` suffix is the same naming
/// that `committee::active::PARITY_MATRIX_INSECURE / _SECURE` re-export from.
fn file_for(root: &Path, committee: &str, preset: BfvPreset) -> PathBuf {
    let suffix = match preset {
        BfvPreset::InsecureThreshold512 => "insecure",
        BfvPreset::SecureThreshold8192 => "secure",
        // Codegen runs against the threshold side of each preset family.
        // DKG-only variants don't need their own file.
        BfvPreset::InsecureDkg512 => "insecure",
        BfvPreset::SecureDkg8192 => "secure",
    };
    root.join(committee).join(format!("parity_{suffix}.nr"))
}

/// File template. Threading `committee` / `preset` / `matrix` keeps the on-disk format
/// stable across regenerations (so the lefthook check can diff against a fresh run).
fn render(committee: &str, preset_suffix: &str, l_module: &str, matrix_literal: &str) -> String {
    let preset_label = match preset_suffix {
        "insecure" => "insecure-512 (L_THRESHOLD=2)",
        "secure" => "secure-8192 (L_THRESHOLD=3)",
        other => other,
    };
    format!(
        "// SPDX-License-Identifier: LGPL-3.0-only\n\
//\n\
// This file is provided WITHOUT ANY WARRANTY;\n\
// without even the implied warranty of MERCHANTABILITY\n\
// or FITNESS FOR A PARTICULAR PURPOSE.\n\
//\n\
// AUTO-GENERATED by `generate_parity_matrices`. DO NOT EDIT BY HAND.\n\
// Regenerate with `pnpm build:circuits --committee {committee}`.\n\
//\n\
// Reed-Solomon parity matrix for the {committee} committee under the\n\
// {preset_label} preset. Dimensions: [L_THRESHOLD][N_PARTIES - T][N_PARTIES + 1].\n\
\n\
use crate::configs::committee::{committee}::{{N_PARTIES, T}};\n\
pub use crate::configs::{l_module}::threshold::L as L_THRESHOLD;\n\
\n\
{matrix_literal}\n"
    )
}

fn main() -> Result<()> {
    let args = Args::parse();
    let size = CiphernodesCommitteeSize::from_str(&args.committee)
        .with_context(|| format!("unknown committee: {}", args.committee))?;
    let params = size.values();
    let root = output_root(&args)?;

    if !root.join(&args.committee).is_dir() {
        anyhow::bail!(
            "expected committee module dir at {}; \
             does {} exist as `pub mod {}` under committee/mod.nr?",
            root.join(&args.committee).display(),
            args.committee,
            args.committee
        );
    }

    for preset in BfvPreset::PAIR_PRESETS {
        let (threshold_params, _) = build_pair_for_preset(preset)
            .with_context(|| format!("build_pair_for_preset({preset:?}) failed"))?;
        let literal = parity_matrix_constant_string(&threshold_params, params.n, params.threshold)
            .with_context(|| {
                format!(
                    "parity_matrix_constant_string(committee={}, preset={preset:?}) failed",
                    args.committee
                )
            })?;
        let (suffix, l_module) = match preset {
            BfvPreset::InsecureThreshold512 => ("insecure", "insecure"),
            BfvPreset::SecureThreshold8192 => ("secure", "secure"),
            _ => continue, // PAIR_PRESETS only carries the threshold variants
        };
        let path = file_for(&root, &args.committee, preset);
        let content = render(&args.committee, suffix, l_module, &literal);
        std::fs::write(&path, content).with_context(|| format!("writing {}", path.display()))?;
        println!(
            "✓ {} ({} rows × {} cols)",
            path.display(),
            params.n - params.threshold,
            params.n + 1
        );
    }

    Ok(())
}
